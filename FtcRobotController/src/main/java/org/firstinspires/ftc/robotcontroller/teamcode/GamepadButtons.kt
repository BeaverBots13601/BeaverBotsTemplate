package org.firstinspires.ftc.robotcontroller.teamcode

/**
 * An enum of literally every button, separated by gamepad number.
 */
enum class GamepadButtons {
    // triggers (gp1)
    GP1_LEFT_TRIGGER,
    GP1_RIGHT_TRIGGER,

    // bumpers (gp1)
    GP1_LEFT_BUMPER,
    GP1_RIGHT_BUMPER,

    // special buttons (gp1)
    GP1_SHARE,
    GP1_OPTIONS,
    GP1_TOUCHPAD,
    GP1_PS,

    // dpad (gp1)
    GP1_DPAD_UP,
    GP1_DPAD_LEFT,
    GP1_DPAD_RIGHT,
    GP1_DPAD_DOWN,

    // buttons (gp1)
    GP1_TRIANGLE,
    GP1_SQUARE,
    GP1_CIRCLE,
    GP1_CROSS,

    // joysticks (gp1)
    GP1_LEFT_JOYSTICK,
    GP1_RIGHT_JOYSTICK,
    GP1_LEFT_JOYSTICK_PRESS,
    GP1_RIGHT_JOYSTICK_PRESS,

    // triggers (gp2)
    GP2_LEFT_TRIGGER,
    GP2_RIGHT_TRIGGER,

    // bumpers (gp2)
    GP2_LEFT_BUMPER,
    GP2_RIGHT_BUMPER,

    // special buttons (gp2)
    GP2_SHARE,
    GP2_OPTIONS,
    GP2_TOUCHPAD,
    GP2_PS,

    // dpad (gp2)
    GP2_DPAD_UP,
    GP2_DPAD_LEFT,
    GP2_DPAD_RIGHT,
    GP2_DPAD_DOWN,

    // buttons (gp2)
    GP2_TRIANGLE,
    GP2_SQUARE,
    GP2_CIRCLE,
    GP2_CROSS,

    // joysticks (gp2)
    GP2_LEFT_JOYSTICK,
    GP2_RIGHT_JOYSTICK,
    GP2_LEFT_JOYSTICK_PRESS,
    GP2_RIGHT_JOYSTICK_PRESS,
}